package Control_Statements___MathClass__String;

public class Mathclass {
    public static void main(String[] args) {
        System.out.println(Math.abs(-99));
        System.out.println(Math.ceil(99.9));
        System.out.println(Math.floor(99.9));
        System.out.println(Math.round(99.99));
        System.out.println(Math.max(99,100));
        System.out.println(Math.min(99,100));
        System.out.println(Math.sqrt(100));
        System.out.println(Math.pow(2,2));
        System.out.println(Math.log10(100));
        System.out.println(Math.exp(2));
    }
}
